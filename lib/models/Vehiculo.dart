class Vehiculo{

  String tipo;
  String marca;
  String modelo;
  String serie;
  String economico;
  String placa;
  int clienteid;

  Vehiculo(tipo, marca, modelo, serie, economico, placa, clienteid){

    this.tipo = tipo;
    this.marca = marca;
    this.modelo = modelo;
    this.serie = serie;
    this.economico = economico;
    this.placa = placa;
    this.clienteid = clienteid;

  }

}